async function main() {
  try {
    console.log('üå± Seeding EFRIS Units of Measure...\n');
    
    // Get the demo organization (or specify your organization slug)
    const orgSlug = process.argv[2] || 'demo-company';
    
    const organization = await prisma.organization.findUnique({
      where: { slug: orgSlug }
    });
    
    if (!organization) {
      console.error(`‚ùå Organization "${orgSlug}" not found!`);
      console.log('Usage: node scripts/seed-efris-units.js [organization-slug]');
      console.log('Example: node scripts/seed-efris-units.js demo-company');
      process.exit(1);
    }
    
    console.log(`üìç Organization: ${organization.name} (${orgSlug})\n`);
    
    let created = 0;
    let updated = 0;
    let skipped = 0;
    
    for (const unit of efrisUnits) {
      try {
        const existing = await prisma.unitOfMeasure.findUnique({
          where: {
            organizationId_code: {
              organizationId: organization.id,
              code: unit.code,
            },
          },
        });
        
        if (existing) {
          // Update existing unit
          await prisma.unitOfMeasure.update({
            where: { id: existing.id },
            data: {
              name: unit.name,
              abbreviation: unit.abbreviation,
              category: unit.category,
            },
          });
          updated++;
          console.log(`‚úì Updated: ${unit.name} (${unit.code})`);
        } else {
          // Create new unit
          await prisma.unitOfMeasure.create({
            data: {
              organizationId: organization.id,
              code: unit.code,
              name: unit.name,
              abbreviation: unit.abbreviation,
              category: unit.category,
              isActive: true,
            },
          });
          created++;
          console.log(`‚úì Created: ${unit.name} (${unit.code})`);
        }
      } catch (error) {
        console.error(`‚úó Error processing ${unit.name}:`, error.message);
        skipped++;
      }
    }
    
    console.log('\n' + '='.repeat(60));
    console.log('‚úÖ Seeding completed!');
    console.log(`   Created: ${created} units`);
    console.log(`   Updated: ${updated} units`);
    console.log(`   Skipped: ${skipped} units`);
    console.log(`   Total: ${efrisUnits.length} units processed`);
    console.log('='.repeat(60) + '\n');
    
  } catch (error) {
    console.error('‚ùå Error:', error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

